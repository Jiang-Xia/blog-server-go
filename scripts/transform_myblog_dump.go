// transform_myblog_dump 将 myblog mysqldump 转为 x_my_blog 可导入 SQL（表名加 x_ 前缀）。
//
//	go run scripts/transform_myblog_dump.go -in D:/data/.../myblog_backup.sql -out deploy/sql/prod/x_my_blog_import.sql
package main

import (
	"bufio"
	"flag"
	"fmt"
	"os"
	"regexp"
	"sort"
	"strings"
)

func main() {
	inPath := flag.String("in", "", "myblog 备份 SQL 路径")
	outPath := flag.String("out", "deploy/sql/prod/x_my_blog_import.sql", "输出 SQL 路径")
	flag.Parse()
	if strings.TrimSpace(*inPath) == "" {
		fmt.Fprintln(os.Stderr, "missing -in")
		os.Exit(1)
	}

	tables, err := extractTables(*inPath)
	if err != nil {
		fail(err)
	}
	sort.Slice(tables, func(i, j int) bool {
		return len(tables[i]) > len(tables[j])
	})

	in, err := os.Open(*inPath)
	if err != nil {
		fail(err)
	}
	defer in.Close()

	if err := os.MkdirAll(dirOf(*outPath), 0o755); err != nil {
		fail(err)
	}
	out, err := os.Create(*outPath)
	if err != nil {
		fail(err)
	}
	defer out.Close()

	w := bufio.NewWriter(out)
	_, _ = fmt.Fprintf(w, "-- Generated by scripts/transform_myblog_dump.go\n")
	_, _ = fmt.Fprintf(w, "-- Source: %s\n", *inPath)
	_, _ = fmt.Fprintf(w, "-- Target database: x_my_blog (tables prefixed with x_)\n")
	_, _ = fmt.Fprintf(w, "-- Import: mysql -u jxblog -p -h 127.0.0.1 x_my_blog < %s\n\n", *outPath)
	_, _ = fmt.Fprintf(w, "USE `x_my_blog`;\n\n")

	replacers := make([][2]string, 0, len(tables))
	for _, t := range tables {
		replacers = append(replacers, [2]string{"`" + t + "`", "`x_" + t + "`"})
	}

	sc := bufio.NewScanner(in)
	const maxLine = 64 << 20 // 64MB per line for huge INSERT
	buf := make([]byte, 0, 1024*1024)
	sc.Buffer(buf, maxLine)

	for sc.Scan() {
		line := sc.Text()
		// 跳过源库注释行，避免误导
		if strings.Contains(line, "Database: myblog") {
			line = strings.ReplaceAll(line, "Database: myblog", "Database: x_my_blog (from myblog dump)")
		}
		for _, r := range replacers {
			line = strings.ReplaceAll(line, r[0], r[1])
		}
		if _, err := fmt.Fprintln(w, line); err != nil {
			fail(err)
		}
	}
	if err := sc.Err(); err != nil {
		fail(err)
	}
	if err := w.Flush(); err != nil {
		fail(err)
	}

	fmt.Printf("tables=%d wrote %s\n", len(tables), *outPath)
}

var createTableRe = regexp.MustCompile("^CREATE TABLE `([^`]+)`")

func extractTables(path string) ([]string, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer f.Close()

	seen := make(map[string]struct{})
	var tables []string
	sc := bufio.NewScanner(f)
	buf := make([]byte, 0, 1024*1024)
	sc.Buffer(buf, 64<<20)
	for sc.Scan() {
		m := createTableRe.FindStringSubmatch(sc.Text())
		if len(m) != 2 {
			continue
		}
		if _, ok := seen[m[1]]; ok {
			continue
		}
		seen[m[1]] = struct{}{}
		tables = append(tables, m[1])
	}
	return tables, sc.Err()
}

func dirOf(path string) string {
	if i := strings.LastIndexAny(path, "/\\"); i >= 0 {
		return path[:i]
	}
	return "."
}

func fail(err error) {
	fmt.Fprintf(os.Stderr, "transform_myblog_dump: %v\n", err)
	os.Exit(1)
}
